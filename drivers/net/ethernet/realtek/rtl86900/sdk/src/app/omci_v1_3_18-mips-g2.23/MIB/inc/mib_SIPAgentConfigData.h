#define MIB_TABLE_SIPAGENTCONFIGDATA_PRIMARYSIPDNS_INDEX ((MIB_ATTR_INDEX)4)
#define MIB_TABLE_SIPAGENTCONFIGDATA_SECONDARYSIPDNS_INDEX ((MIB_ATTR_INDEX)5)
